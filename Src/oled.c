/**
 * @brief 最终修复版字符显示函数（索引偏移-1）
 * @param x 横坐标(0~120)
 * @param y 页坐标(0~7)
 * @param ch 要显示的字符
 */
void OLED_ShowChar(uint8_t x, uint8_t y, uint8_t ch)
{
    // 1. 边界校验
    if(x > OLED_WIDTH - 8 || y > OLED_PAGE_NUM - 1)
    {
        return;
    }

    // 2. 核心修复：索引计算 = ASCII码 - 0x21（因为点阵库索引0对应0x21）
    uint8_t ascii_code = (uint8_t)ch;
    uint8_t char_idx = 0;
    
    // 处理可打印字符（0x21~0x7E），空格单独处理
    if(ascii_code == 0x20) // 空格(0x20)：显示全0点阵
    {
        char_idx = 255; // 标记为空格，后续填充0
    }
    else if(ascii_code >= 0x21 && ascii_code <= 0x7E)
    {
        char_idx = ascii_code - 0x21; // 关键：偏移修正为-0x21
    }
    else // 非可打印字符：显示空格
    {
        char_idx = 255;
    }

    // 3. 读取点阵数据（空格则填充0）
    uint8_t char_data[8] = {0}; // 默认全0（空格）
    if(char_idx != 255 && char_idx < 96) // 确保索引不越界
    {
        for(uint8_t i=0; i<8; i++)
        {
            char_data[i] = OLED_F8x8[char_idx][i];
        }
    }
    
    // 4. 发送到OLED
    OLED_SetPos(y, x);
    OLED_WriteBuffer(OLED_WR_DATA, char_data, 8);
}

/**
 * @brief 修复后的字符串显示函数
 * @param x 起始横坐标
 * @param y 起始页坐标
 * @param str 要显示的字符串
 */
void OLED_ShowString(uint8_t x, uint8_t y, char *str)
{
    if(str == NULL || x > OLED_WIDTH-8 || y > OLED_PAGE_NUM-1)
    {
        return;
    }

    uint8_t current_x = x;
    while(*str != '\0' && current_x <= OLED_WIDTH-8)
    {
        OLED_ShowChar(current_x, y, (uint8_t)*str);
        current_x += 8;
        str++;
    }
}

void OLED_ShowNum(uint8_t x, uint8_t y, int32_t num, uint8_t len)
{
    // 1. 边界校验
    if(x > OLED_WIDTH - 8 || y > OLED_PAGE_NUM - 1 || len == 0 || len > 8)
    {
        return;
    }

    uint8_t num_buf[8] = {0}; // 存储每位数字的ASCII码
    uint8_t is_negative = 0;  // 负数标记
    uint32_t abs_num = 0;     // 绝对值

    // 2. 处理负数
    if(num < 0)
    {
        is_negative = 1;
        abs_num = (uint32_t)-num;
    }
    else
    {
        abs_num = (uint32_t)num;
    }

    // 3. 拆分数字到数组（低位到高位）
    for(uint8_t i=0; i<len; i++)
    {
        num_buf[len-1-i] = abs_num % 10 + '0'; // 转为ASCII码
        abs_num /= 10;
    }

    // 4. 处理负数符号
    uint8_t current_x = x;
    if(is_negative)
    {
        OLED_ShowChar(current_x, y, '-'); // 显示负号
        current_x += 8;
        if(current_x > OLED_WIDTH - 8) return;
    }

    // 5. 逐位显示数字
    for(uint8_t i=0; i<len; i++)
    {
        if(current_x > OLED_WIDTH - 8) break;
        OLED_ShowChar(current_x, y, num_buf[i]);
        current_x += 8;
    }
}

void OLED_ShowFloat(uint8_t x, uint8_t y, float num, uint8_t int_len, uint8_t dec_len)
{
    // 1. 边界校验（防止越界显示）
    if(x > OLED_WIDTH - 8 || y > OLED_PAGE_NUM - 1 || int_len == 0 || dec_len == 0 ||
       int_len > 7 || dec_len > 3 || (int_len + dec_len + 1) * 8 > (OLED_WIDTH - x))
    {
        return;
    }

    uint8_t current_x = x;
    uint8_t is_negative = 0;  // 负数标记
    float abs_num = num;      // 绝对值

    // 2. 处理负数
    if(num < 0)
    {
        is_negative = 1;
        abs_num = -num;       // 转为正数处理
    }

    // 3. 拆分整数部分和小数部分
    // 3. 拆分整数部分和小数部分
uint32_t int_part = (uint32_t)abs_num;                  // 整数部分（取整）

// 计算10的dec_len次方
uint32_t pow10 = 1;
for(uint8_t i = 0; i < dec_len; i++) {
    pow10 *= 10;
}
uint32_t dec_part = (uint32_t)((abs_num - int_part) * pow10 + 0.5f); // 小数部分（四舍五入）

    // 4. 显示负号（如果是负数）
    if(is_negative)
    {
        OLED_ShowChar(current_x, y, '-');
        current_x += 8;
        if(current_x > OLED_WIDTH - 8) return;
    }

    // 5. 显示整数部分
    OLED_ShowNum(current_x, y, int_part, int_len);
    current_x += int_len * 8;
    if(current_x > OLED_WIDTH - 8) return;

    // 6. 显示小数点
    OLED_ShowChar(current_x, y, '.');
    current_x += 8;
    if(current_x > OLED_WIDTH - 8) return;

    // 7. 显示小数部分（补零对齐）
    OLED_ShowNum(current_x, y, dec_part, dec_len);
}

// -------------------------- 底层函数（保持不变） --------------------------
void OLED_SetPos(uint8_t y,uint8_t x)
{
    OLED_WriteByte(OLED_WR_CMD, 0xB0 + y);                  
    OLED_WriteByte(OLED_WR_CMD, 0x00 + (x & 0x0F));         
    OLED_WriteByte(OLED_WR_CMD, 0x10 + ((x >> 4) & 0x0F));  
}

ErrorStatus OLED_CheckDevice(uint8_t slave_addr)
{
    uint8_t rdm=0;
    if (HAL_I2C_Master_Transmit(&hi2c1,(slave_addr<<1),&rdm,sizeof(rdm),IIC_TIMEOUT)==HAL_OK)
    {
        return SUCCESS;
    }
    return ERROR;
}

ErrorStatus OLED_WriteByte(uint8_t cmd,uint8_t byte)
{
    uint8_t buf[2]={cmd,byte};
    if(HAL_I2C_Master_Transmit(&hi2c1,SLAVE_ADDR<<1,buf,2,IIC_TIMEOUT)==HAL_OK)
    {
        return SUCCESS;
    }
    return ERROR;
}

ErrorStatus OLED_WriteBuffer(uint8_t cmd,uint8_t* buffer,uint32_t num)
{
    uint8_t buf[256];
    if(num>sizeof(buf)-1)
    {
        return ERROR;
    }
    buf[0]=cmd;
    uint16_t i;
    for(i=0;i<num;i+=1)
    {
        buf[i+1]=buffer[i];
    }
    if(HAL_I2C_Master_Transmit(&hi2c1,SLAVE_ADDR<<1,buf,num+1,IIC_TIMEOUT)==HAL_OK)
    {
        return SUCCESS;
    }
    else 
    {return ERROR;}
}

void OLED_Fill(uint8_t fill_data)
{
    uint8_t data_buffer_temp[128];
    uint16_t i;
    for (i=0;i<128;i+=1)
    {
        data_buffer_temp[i]=fill_data;
    }
    for (i=0;i<8;i++)
    {
        OLED_SetPos(i,0);
        OLED_WriteBuffer(OLED_WR_DATA,data_buffer_temp,sizeof(data_buffer_temp));
    }
}

void OLED_Clear(void)
{
    OLED_Fill(0x00);
}

void OLED_FullFill(void)
{
    OLED_Fill(0xFF);
}

